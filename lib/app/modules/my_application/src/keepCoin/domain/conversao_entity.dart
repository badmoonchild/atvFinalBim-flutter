class Conversao {
  final String? id;
  final String? nome;
  final double? dolar;
  final double? real;
  final String? tipo;
  final String? compra;
  final String? venda;
  final String? variacao;
  final String? pctVariacao;

  Conversao({
    this.id,
    required this.nome,
    required this.dolar,
    required this.real,
    required this.tipo,
    required this.compra,
    required this.venda,
    required this.variacao,
    required this.pctVariacao,
  });
}

